package com.zc.superbooks.entity;

public class Distribution {
	private String total;
	private String crash;
	private String alipay;
	private String card;
	private String qqWallet;
	private String weixinWallet;
	private String financialProducts;
	
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	public String getCrash() {
		return crash;
	}
	public void setCrash(String crash) {
		this.crash = crash;
	}
	public String getAlipay() {
		return alipay;
	}
	public void setAlipay(String alipay) {
		this.alipay = alipay;
	}
	public String getCard() {
		return card;
	}
	public void setCard(String card) {
		this.card = card;
	}
	public String getQqWallet() {
		return qqWallet;
	}
	public void setQqWallet(String qqWallet) {
		this.qqWallet = qqWallet;
	}
	public String getWeixinWallet() {
		return weixinWallet;
	}
	public void setWeixinWallet(String weixinWallet) {
		this.weixinWallet = weixinWallet;
	}
	public String getFinancialProducts() {
		return financialProducts;
	}
	public void setFinancialProducts(String financialProducts) {
		this.financialProducts = financialProducts;
	}
}
