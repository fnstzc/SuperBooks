package com.zc.superbooks.entity;

public class Member {
	private String name;
	private String fortune;
	private String identity;
	private String cost;
	private String monthCost;
	private String yearCost;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getFortune() {
		return fortune;
	}
	public void setFortune(String fortune) {
		this.fortune = fortune;
	}
	public String getIdentity() {
		return identity;
	}
	public void setIdentity(String identity) {
		this.identity = identity;
	}
	public String getCost() {
		return cost;
	}
	public void setCost(String cost) {
		this.cost = cost;
	}
	public String getMonthCost() {
		return monthCost;
	}
	public void setMonthCost(String monthCost) {
		this.monthCost = monthCost;
	}
	public String getYearCost() {
		return yearCost;
	}
	public void setYearCost(String yearCost) {
		this.yearCost = yearCost;
	}
}
