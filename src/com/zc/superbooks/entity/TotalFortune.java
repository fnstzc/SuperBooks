package com.zc.superbooks.entity;

public class TotalFortune {
	private String fortune;
	private String totalCost;
	private String totalIncome;
	private String pastCost;
	private String pastIncome;
	private String rate;
	private String date;
	
	public String getFortune() {
		return fortune;
	}
	public void setFortune(String fortune) {
		this.fortune = fortune;
	}
	public String getTotalCost() {
		return totalCost;
	}
	public void setTotalCost(String totalCost) {
		this.totalCost = totalCost;
	}
	public String getTotalIncome() {
		return totalIncome;
	}
	public void setTotalIncome(String totalIncome) {
		this.totalIncome = totalIncome;
	}
	public String getPastCost() {
		return pastCost;
	}
	public void setPastCost(String pastCost) {
		this.pastCost = pastCost;
	}
	public String getPastIncome() {
		return pastIncome;
	}
	public void setPastIncome(String pastIncome) {
		this.pastIncome = pastIncome;
	}
	public String getRate() {
		return rate;
	}
	public void setRate(String rate) {
		this.rate = rate;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
}
